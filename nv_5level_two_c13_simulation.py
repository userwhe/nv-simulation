def save_csv(result: dict, path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as f:
        w = csv.writer(f)
        w.writerow([
            "t_us",
            "bell_fidelity_without_protocol",
            "decoded_bell_fidelity_with_qec",
            "recovery_count_applied",
        ])
        for row in zip(
            result["t_us"],
            result["fidelity_without_protocol"],
            result["fidelity_with_protocol"],
            result["recovery_count"],
        ):
            w.writerow([f"{row[0]:.12g}", f"{row[1]:.12g}", f"{row[2]:.12g}", int(row[3])])


def _spin_text(selected: list[DQPSpin]) -> str:
    return ", ".join(
        f"#{s.index}: -A_par={s.minus_A_par_kHz:.3g} kHz, A_perp={s.A_perp_kHz:.3g} kHz" for s in selected
    )


def plot_overlay(result: dict, selected: list[DQPSpin], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fig = plt.figure(figsize=(7.4, 4.8))
    ax = fig.add_subplot(111)
    ax.plot(result["t_us"], result["fidelity_without_protocol"], label=r"Without protocol: physical $|\Psi^+\rangle$")

    if result["mode"] == "periodic":
        label = f"With QEC: ideal recovery every {result['recovery_interval_us']:.3g} µs"
        title = "Bell-state fidelity with realistic-cadence QEC recovery"
        box_line = f"recovery interval = {result['recovery_interval_us']:.3g} µs"
    elif result["mode"] == "single":
        label = "With QEC: exactly one ideal recovery"
        title = "Bell-state fidelity with one QEC recovery operation"
        box_line = f"single recovery at t = {result['single_recovery_time_us']:.3g} µs"
        ax.axvline(result["single_recovery_time_us"], linestyle="--", linewidth=1.0, label="single recovery time")
    else:
        label = "With QEC protocol"
        title = "Two-13C Bell-state fidelity with common-fluctuator QEC"
        box_line = ""

    ax.plot(result["t_us"], result["fidelity_with_protocol"], label=label)
    ax.set_xlabel("Time (µs)")
    ax.set_ylabel("Bell-state fidelity")
    ax.set_title(title)
    ax.set_ylim(0.0, 1.02)
    ax.grid(True, alpha=0.3)
    ax.legend(loc="best")
    ax.text(
        0.02,
        0.04,
        f"DQP spins {_spin_text(selected)}\n{box_line}\nideal instantaneous recovery channel; gate errors not included",
        transform=ax.transAxes,
        fontsize=8.2,
        va="bottom",
        bbox={"boxstyle": "round", "alpha": 0.12},
    )
    fig.tight_layout()
    fig.savefig(path, dpi=220)
    plt.close(fig)


def run_sanity_checks(code: QECCode, results: list[dict]) -> None:
    basis = np.column_stack([code.zero_L, code.one_L, code.zero_E, code.one_E])
    orth_err = float(np.max(np.abs(basis.conj().T @ basis - np.eye(4))))
    cptp_err = float(np.max(np.abs(sum(K.conj().T @ K for K in code.recovery_kraus) - np.eye(4))))
    trace_err = max(float(np.max(np.abs(r["trace_with_protocol"] - 1.0))) for r in results)
    fid_low = min(float(np.min(r["fidelity_with_protocol"])) for r in results)
    fid_high = max(float(np.max(r["fidelity_with_protocol"])) for r in results)
    if orth_err > 1e-12 or cptp_err > 1e-12 or trace_err > 1e-8 or fid_low < -1e-8 or fid_high > 1.0 + 1e-8:
        raise RuntimeError(
            "Sanity check failed: "
            f"orth={orth_err:.3e}, cptp={cptp_err:.3e}, trace={trace_err:.3e}, "
            f"fid_range=({fid_low:.3e}, {fid_high:.3e})"
        )


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--dqp-file", default="dqpspins.json")
    p.add_argument("--selection", choices=["strongest", "closest-positive"], default="strongest")
    p.add_argument("--spin-indices", nargs=2, type=int, metavar=("I", "J"), help="1-based DQP spin indices")
    p.add_argument("--time-us", type=float, default=20.0)
    p.add_argument("--plot-dt-us", type=float, default=0.5, help="time spacing used only for plotting/data output")
    p.add_argument(
        "--recovery-interval-us",
        type=float,
        default=10.0,
        help="periodic QEC recovery cadence in microseconds; default is a less idealized 10 us",
    )
    p.add_argument(
        "--single-recovery-time-us",
        type=float,
        default=None,
        help="time of the one-and-only recovery operation; default is half of --time-us",
    )
    p.add_argument("--B-T", type=float, default=0.05)
    p.add_argument("--pump-rate", type=float, default=5.0)
    p.add_argument("--output-dir", default="outputs")
    p.add_argument("--skip-checks", action="store_true")
    return p.parse_args()


def main() -> None:
    args = parse_args()
    if args.time_us <= 0 or args.plot_dt_us <= 0:
        raise ValueError("--time-us and --plot-dt-us must be positive")

    spins = load_dqp_spins(args.dqp_file)
    selected = choose_two_spins(spins, args.selection, tuple(args.spin_indices) if args.spin_indices else None)
    params = model_params_from_spins(selected, args.B_T, args.pump_rate)
    code = make_two_qubit_qec_code(tuple(s.g_MHz for s in selected))

    tlist = np.arange(0.0, args.time_us + 0.5 * args.plot_dt_us, args.plot_dt_us)
    single_time = args.single_recovery_time_us if args.single_recovery_time_us is not None else 0.5 * args.time_us

    periodic = simulate_periodic_recovery(tlist, params, code, args.recovery_interval_us)
    single = simulate_single_recovery_trace(tlist, params, code, single_time)
    if not args.skip_checks:
        run_sanity_checks(code, [periodic, single])

    out = Path(args.output_dir)
    periodic_png = out / "qec_bell_fidelity_periodic_realistic_interval.png"
    periodic_svg = out / "qec_bell_fidelity_periodic_realistic_interval.svg"
    periodic_csv = out / "qec_bell_fidelity_periodic_realistic_interval.csv"
    single_png = out / "qec_bell_fidelity_single_recovery.png"
    single_svg = out / "qec_bell_fidelity_single_recovery.svg"
    single_csv = out / "qec_bell_fidelity_single_recovery.csv"

    plot_overlay(periodic, selected, periodic_png)
    plot_overlay(periodic, selected, periodic_svg)
    save_csv(periodic, periodic_csv)
    plot_overlay(single, selected, single_png)
    plot_overlay(single, selected, single_svg)
    save_csv(single, single_csv)

    print("Simulation completed.")
    print("Selected DQP spins, ordered as |g1| >= |g2|:")
    for s in selected:
        print(
            f"  spin #{s.index}: -A_parallel={s.minus_A_par_kHz:.9g} kHz, "
            f"A_parallel={-s.minus_A_par_kHz:.9g} kHz, A_perp={s.A_perp_kHz:.9g} kHz"
        )
    print(f"QEC couplings: g1={code.g_MHz[0]:.9g} MHz, g2={code.g_MHz[1]:.9g} MHz")
    print(f"Plot dt: {args.plot_dt_us:.9g} us")
    print(f"Periodic recovery interval: {periodic['recovery_interval_us']:.9g} us")
    print(f"Single recovery time: {single['single_recovery_time_us']:.9g} us")
    print("Final Bell-state fidelities at t = {:.9g} us:".format(tlist[-1]))
    print(f"  without protocol                         : {periodic['fidelity_without_protocol'][-1]:.6f}")
    print(f"  periodic QEC, decoded                    : {periodic['fidelity_with_protocol'][-1]:.6f}")
    print(f"  periodic improvement                     : {periodic['fidelity_with_protocol'][-1] - periodic['fidelity_without_protocol'][-1]:+.6f}")
    print(f"  single-recovery QEC trace, decoded       : {single['fidelity_with_protocol'][-1]:.6f}")
    print(f"  single-recovery improvement              : {single['fidelity_with_protocol'][-1] - single['fidelity_without_protocol'][-1]:+.6f}")
    print("Saved:")
    for path in [periodic_png, periodic_svg, periodic_csv, single_png, single_svg, single_csv]:
        print(f"  {path}")


if __name__ == "__main__":
    main()
