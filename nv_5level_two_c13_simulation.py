def load_dqp_spins(path: str | Path | None) -> list[DQPSpin]:
    if path is None or not Path(path).exists():
        raw = DEFAULT_DQP_SPINS
    else:
        # Allow a trailing comma in the JSON-like file.
        text = re.sub(r",\s*([\]\}])", r"\1", Path(path).read_text())
        raw = json.loads(text)
    return [DQPSpin(i, float(pair[0]), float(pair[1])) for i, pair in enumerate(raw, start=1)]


def choose_two_spins(
    spins: list[DQPSpin],
    strategy: str = "strongest",
    spin_indices: tuple[int, int] | None = None,
) -> list[DQPSpin]:
    if spin_indices is not None:
        by_index = {s.index: s for s in spins}
        missing = [idx for idx in spin_indices if idx not in by_index]
        if missing:
            raise ValueError(f"Unknown DQP spin indices: {missing}")
        selected = [by_index[i] for i in spin_indices]
    elif strategy == "strongest":
        selected = sorted(spins, key=lambda s: abs(s.minus_A_par_kHz), reverse=True)[:2]
    elif strategy == "closest-positive":
        pairs = []
        for i, a in enumerate(spins):
            for b in spins[i + 1 :]:
                if a.minus_A_par_kHz > 0 and b.minus_A_par_kHz > 0:
                    mismatch = abs(a.minus_A_par_kHz - b.minus_A_par_kHz) / max(
                        abs(a.minus_A_par_kHz), abs(b.minus_A_par_kHz)
                    )
                    strength = 0.5 * (abs(a.minus_A_par_kHz) + abs(b.minus_A_par_kHz))
                    pairs.append((mismatch, -strength, a, b))
        if not pairs:
            raise ValueError("No positive same-sign DQP spin pair found")
        _, _, a, b = min(pairs, key=lambda row: (row[0], row[1]))
        selected = [a, b]
    else:
        raise ValueError(f"Unknown spin-selection strategy: {strategy}")

    return sorted(selected, key=lambda s: abs(s.minus_A_par_kHz), reverse=True)


def make_two_qubit_qec_code(g_MHz: tuple[float, float]) -> QECCode:
    """Build the n=2 hardware-efficient CFD code and recovery channel.

    The code assumes |g1| >= |g2|, as in the paper.  A global sign flip of both
    couplings only relabels logical phases, so the construction normalizes g1 > 0.
    """

    g = np.asarray(g_MHz, dtype=float)
    if abs(g[0]) < abs(g[1]) - 1e-15:
        raise ValueError("Order spins so |g1| >= |g2|")
    if g[0] < 0:
        g = -g
    g1, g2 = float(g[0]), float(g[1])
    if g1 <= 0 or g1 + g2 < -1e-14 or g1 - g2 < -1e-14:
        raise ValueError("The n=2 construction requires g1 > 0 and |g2| <= g1 after sign normalization")

    # Eq. (9) normalized: a^2=(g1-g2)/(2g1), b^2=(g1+g2)/(2g1).
    a = math.sqrt(max(0.0, (g1 - g2) / (2 * g1)))
    b = math.sqrt(max(0.0, (g1 + g2) / (2 * g1)))

    # Convenient phase choice making chi0 and chi1 orthogonal and giving a
    # separable syndrome operator S = U_z \otimes Z_2.
    chi0 = np.array([a, b], dtype=complex)
    chi1 = np.array([-b, a], dtype=complex)
    zero, one = ket(2, 0), ket(2, 1)

    zero_L = np.kron(chi0, zero)
    one_L = np.kron(chi1, one)
    zero_E = np.kron(chi1, zero)
    one_E = np.kron(chi0, one)

    P_L = density(zero_L) + density(one_L)
    P_E = density(zero_E) + density(one_E)
    U_x = np.outer(chi0, chi1.conj()) + np.outer(chi1, chi0.conj())

    # Transpose/standard recovery for the n=2 case:
    # no-error syndrome leaves C0 fixed; error syndrome maps C1 back to C0.
    K_no_error = P_L
    K_error = np.kron(U_x, np.eye(2, dtype=complex)) @ P_E

    # Encoding maps computational |01> and |10> to |0_L> and |1_L>.
    # This makes U_enc |Psi+> = (|0_L> + |1_L>)/sqrt(2).
    U_enc = np.column_stack([zero_E, zero_L, one_L, one_E])
    return QECCode((g1, g2), (K_no_error, K_error), U_enc, U_enc.conj().T, zero_L, one_L, zero_E, one_E)


def model_params_from_spins(spins: list[DQPSpin], B_T: float, pump_rate: float) -> dict:
    return {
        "B_T": B_T,
        "A_par_MHz": tuple(s.A_par_MHz for s in spins),
        "A_perp_MHz": tuple(s.A_perp_MHz for s in spins),
        "W_pump_per_us": pump_rate,
        "interaction_picture": True,
    }


# -----------------------------------------------------------------------------
# QEC evolution modes
# -----------------------------------------------------------------------------


def apply_recovery_to_full_state(rho: np.ndarray, code: QECCode, de: int) -> np.ndarray:
    out = np.zeros_like(rho)
    I_e = np.eye(de, dtype=complex)
    for K_n in code.recovery_kraus:
        K = np.kron(I_e, K_n)
        out += K @ rho @ K.conj().T
    return hermitize(out)


def decoded_bell_fidelity(rho: np.ndarray, code: QECCode, de: int, dn: int) -> float:
    rho_n = partial_trace_electron(rho, de, dn)
    rho_decoded = code.decoding_unitary @ rho_n @ code.decoding_unitary.conj().T
    return bell_fidelity(rho_decoded)


def _base_no_protocol_curve(L: sp.csr_matrix, tlist_us: np.ndarray, de: int, dn: int) -> np.ndarray:
    d = de * dn
    e0, bell = ket(de, 0), bell_state_psi_plus()
    rho0 = density(np.kron(e0, bell))
    raw_vecs = evolve_liouvillian(L, rho0, tlist_us)
    return np.array([
        bell_fidelity(partial_trace_electron(hermitize(mat(v, d)), de, dn), bell)
        for v in raw_vecs
    ])


def _evolve_density_to_local_times(
    L: sp.csr_matrix,
    rho_start: np.ndarray,
    local_times_us: np.ndarray,
    dim: int,
) -> list[np.ndarray]:
    """Return rho(local_t) for each local_t, all evolved from the same rho_start."""

    local_times_us = np.asarray(local_times_us, dtype=float)
    if len(local_times_us) == 0:
        return []
    if np.any(local_times_us < -1e-12):
        raise ValueError("local_times_us must be nonnegative")
    if len(local_times_us) == 1:
        return [propagate_density(L, rho_start, float(local_times_us[0]), dim)]

    # expm_multiply can generate a whole uniform segment in one Krylov run.
    dt = np.diff(local_times_us)
    if np.allclose(dt, dt[0], rtol=1e-10, atol=1e-12):
        vecs = spla.expm_multiply(
            L,
            vec(rho_start),
            start=float(local_times_us[0]),
            stop=float(local_times_us[-1]),
            num=len(local_times_us),
            endpoint=True,
        )
        return [hermitize(mat(v, dim)) for v in vecs]

    # Fallback for an arbitrary grid.  This path is rarely used for the default
    # settings because recovery times are aligned to the plot grid.
    return [propagate_density(L, rho_start, float(t), dim) for t in local_times_us]


def _store_state_arrays(
    states: list[np.ndarray],
    indices: np.ndarray,
    f_with: np.ndarray,
    tr_with: np.ndarray,
    recovery_count: np.ndarray,
    code: QECCode,
    de: int,
    dn: int,
    count: int,
) -> None:
    for idx, rho_t in zip(indices, states):
        f_with[idx] = decoded_bell_fidelity(rho_t, code, de, dn)
        tr_with[idx] = float(np.real(np.trace(rho_t)))
        recovery_count[idx] = count


def simulate_periodic_recovery(
    tlist_us: np.ndarray,
    model_params: dict,
    code: QECCode,
    recovery_interval_us: float,
) -> dict:
    """Evolution with ideal QEC recovery applied every recovery_interval_us."""

    tlist_us = np.asarray(tlist_us, dtype=float)
    if recovery_interval_us <= 0:
        raise ValueError("recovery_interval_us must be positive")
    if abs(tlist_us[0]) > 1e-15:
        raise ValueError("tlist_us must start at 0")

    H, C, rates, (de, dn) = make_nv_two_c13_model(**model_params)
    L = lindblad_liouvillian(H, C)
    d = de * dn
    f_without = _base_no_protocol_curve(L, tlist_us, de, dn)

    e0, bell = ket(de, 0), bell_state_psi_plus()
    rho_start = density(np.kron(e0, code.encoding_unitary @ bell))
    f_with = np.zeros(len(tlist_us))
    tr_with = np.zeros(len(tlist_us))
    recovery_count = np.zeros(len(tlist_us), dtype=int)

    # t=0 value, before any recovery.
    f_with[0] = decoded_bell_fidelity(rho_start, code, de, dn)
    tr_with[0] = float(np.real(np.trace(rho_start)))

    t_end = float(tlist_us[-1])
    seg_start = 0.0
    count = 0
    tol = 1e-12

    while seg_start < t_end - tol:
        seg_end = min(seg_start + recovery_interval_us, t_end)
        output_indices = np.where((tlist_us > seg_start + tol) & (tlist_us <= seg_end + tol))[0]
        output_times = tlist_us[output_indices]

        need_recovery = seg_end < t_end + tol and abs(seg_end - (seg_start + recovery_interval_us)) <= 1e-9
        seg_end_is_output = len(output_times) > 0 and abs(output_times[-1] - seg_end) <= 1e-9

        # Propagate to all output times in this segment, and also to seg_end if
        # a recovery is needed there but seg_end is not on the output grid.
        eval_times = list(output_times)
        if need_recovery and not seg_end_is_output:
            eval_times.append(seg_end)
        eval_times = np.array(eval_times, dtype=float)
        local_times = eval_times - seg_start
        states = _evolve_density_to_local_times(L, rho_start, local_times, d) if len(eval_times) else []

        # Map eval states back to output states before the recovery at seg_end.
        state_by_time = {round(float(t), 12): rho for t, rho in zip(eval_times, states)}
        pre_recovery_output_indices = output_indices
        pre_recovery_states = [state_by_time[round(float(t), 12)] for t in output_times]
        _store_state_arrays(pre_recovery_states, pre_recovery_output_indices, f_with, tr_with, recovery_count, code, de, dn, count)

        # Apply recovery at the segment end and overwrite the boundary output
        # point with the post-recovery fidelity when seg_end is sampled.
        if need_recovery:
            rho_end = state_by_time.get(round(float(seg_end), 12))
            if rho_end is None:
                rho_end = propagate_density(L, rho_start, seg_end - seg_start, d)
            rho_start = apply_recovery_to_full_state(rho_end, code, de)
            count += 1
            boundary_matches = np.where(np.abs(tlist_us - seg_end) <= 1e-9)[0]
            if len(boundary_matches):
                idx = int(boundary_matches[0])
                f_with[idx] = decoded_bell_fidelity(rho_start, code, de, dn)
                tr_with[idx] = float(np.real(np.trace(rho_start)))
                recovery_count[idx] = count
        else:
            rho_start = states[-1] if states else rho_start

        seg_start = seg_end

    return {
        "mode": "periodic",
        "t_us": tlist_us,
        "fidelity_without_protocol": f_without,
        "fidelity_with_protocol": f_with,
        "trace_with_protocol": tr_with,
        "recovery_count": recovery_count,
        "recovery_interval_us": float(recovery_interval_us),
        "single_recovery_time_us": None,
        "rates": rates,
    }


def simulate_single_recovery_trace(
    tlist_us: np.ndarray,
    model_params: dict,
    code: QECCode,
    single_recovery_time_us: float,
) -> dict:
    """Evolution with exactly one ideal QEC recovery at single_recovery_time_us."""

    tlist_us = np.asarray(tlist_us, dtype=float)
    if single_recovery_time_us <= 0 or single_recovery_time_us > tlist_us[-1] + 1e-12:
        raise ValueError("single_recovery_time_us must lie inside the plotted time window")
    if abs(tlist_us[0]) > 1e-15:
        raise ValueError("tlist_us must start at 0")

    H, C, rates, (de, dn) = make_nv_two_c13_model(**model_params)
    L = lindblad_liouvillian(H, C)
    d = de * dn
    f_without = _base_no_protocol_curve(L, tlist_us, de, dn)

    e0, bell = ket(de, 0), bell_state_psi_plus()
    rho0 = density(np.kron(e0, code.encoding_unitary @ bell))
    f_with = np.zeros(len(tlist_us))
    tr_with = np.zeros(len(tlist_us))
    recovery_count = np.zeros(len(tlist_us), dtype=int)

    tol = 1e-12
    f_with[0] = decoded_bell_fidelity(rho0, code, de, dn)
    tr_with[0] = float(np.real(np.trace(rho0)))

    # Segment before and including the recovery time.
    before_indices = np.where((tlist_us > 0.0 + tol) & (tlist_us <= single_recovery_time_us + tol))[0]
    before_times = tlist_us[before_indices]
    recovery_is_output = len(before_times) > 0 and abs(before_times[-1] - single_recovery_time_us) <= 1e-9
    eval_times = list(before_times)
    if not recovery_is_output:
        eval_times.append(single_recovery_time_us)
    eval_times = np.array(eval_times, dtype=float)
    before_states = _evolve_density_to_local_times(L, rho0, eval_times, d) if len(eval_times) else []
    state_by_time = {round(float(t), 12): rho for t, rho in zip(eval_times, before_states)}

    before_output_states = [state_by_time[round(float(t), 12)] for t in before_times]
    _store_state_arrays(before_output_states, before_indices, f_with, tr_with, recovery_count, code, de, dn, 0)

    rho_at_recovery = state_by_time.get(round(float(single_recovery_time_us), 12))
    if rho_at_recovery is None:
        rho_at_recovery = propagate_density(L, rho0, single_recovery_time_us, d)
    rho_after_recovery = apply_recovery_to_full_state(rho_at_recovery, code, de)

    # If the recovery time is sampled, overwrite it with the post-recovery value.
    boundary_matches = np.where(np.abs(tlist_us - single_recovery_time_us) <= 1e-9)[0]
    if len(boundary_matches):
        idx = int(boundary_matches[0])
        f_with[idx] = decoded_bell_fidelity(rho_after_recovery, code, de, dn)
        tr_with[idx] = float(np.real(np.trace(rho_after_recovery)))
        recovery_count[idx] = 1

    # Segment after the recovery.
    after_indices = np.where(tlist_us > single_recovery_time_us + tol)[0]
    after_times = tlist_us[after_indices]
    local_after = after_times - single_recovery_time_us
    after_states = _evolve_density_to_local_times(L, rho_after_recovery, local_after, d) if len(after_times) else []
    _store_state_arrays(after_states, after_indices, f_with, tr_with, recovery_count, code, de, dn, 1)

    return {
        "mode": "single",
        "t_us": tlist_us,
        "fidelity_without_protocol": f_without,
        "fidelity_with_protocol": f_with,
        "trace_with_protocol": tr_with,
        "recovery_count": recovery_count,
        "recovery_interval_us": None,
        "single_recovery_time_us": float(single_recovery_time_us),
        "rates": rates,
    }


