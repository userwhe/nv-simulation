"""Five-level NV-center + two 13C Bell-state QEC simulation.

This script uses DQP rows of the form (-A_parallel, A_perp) in kHz,
selects two nuclear spins, embeds them in a five-level NV Lindblad model,
and compares Bell-state fidelity with and without the n=2
common-fluctuator dephasing recovery protocol of Layden, Chen, and
Cappellaro.

Two QEC timing modes are generated by default:

1. Periodic recovery at a realistic configurable cadence
   (--recovery-interval-us, default 10 us).  The recovery channel is still
   modeled as ideal and instantaneous; the longer cadence is a timing proxy
   for a recovery that itself requires several one- and two-qubit gates.

2. A single-recovery trace with exactly one recovery operation at
   --single-recovery-time-us (default: half of --time-us).  A vertical line
   is drawn at that recovery time.

Units:
    time: microseconds
    Hamiltonian angular frequencies: rad / microsecond
    input A values: kHz in dqpspins.json, converted to MHz then angular units
"""

from __future__ import annotations

import argparse
import csv
